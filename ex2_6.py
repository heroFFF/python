import turtle
import turtle as t
t.setup(650,350,200,200)
t.penup()
t.fd(-60)
t.pendown()
t.pensize(5)
t.pencolor("yellow")
t.seth(-90)
t.fd(30)
t.penup()
t.fd(30)
t.seth(0)
for i in (0,90,180):
    t.seth(i)
    t.fd(30)
    t.pendown()
    t.fd(60)
    t.penup()
    t.fd(30)
t.seth(-90)
t.fd(30)
t.pendown()
t.fd(60)
